'''
Coleta os 4 valores, em inteiro.
Faz o cálculo da diferença dos produtos.
Imprime o resultado na tela.
'''

a = int(input())  # Coleta a, em inteiro
b = int(input())  # Coleta b, em inteiro
c = int(input())  # Coleta c, em inteiro
d = int(input())  # Coleta d, em inteiro

diference = a*b - c*d  # Diferença entre os produtos

print('DIFERENCA = {}'.format(diference))  # Imprime resultado na tela